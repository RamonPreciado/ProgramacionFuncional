def sumar(a:int|float, b:int|float)->int|float: #Se pasa un argumento ya sea de tipo int o de tipo float y va retornar un valor int o float
    return a + b #Se devuelve el resultado de la operacion

def restar(a:int|float, b:int|float)->int|float: 
    return a- b 
def multiplicar(a:int|float,b:int|float)->int|float: 
    return a*b 

def dividir(a:int|float, b:int|float)->int|float: 
    return a/b 
def cuadrado(a:int|float)->int|float: 
    return a*a 
